package local.java.basics;

public class basics_33{
    public static void main(String[] args){
        double x = 10;
        double y = 3;
        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
        System.out.println("x + y = " + (x + y));
        System.out.println(("x - y = ") + (x - y));
        System.out.println(("x * y = ") + (x * y));
        System.out.println(("x / y = ") + (x / y));
        // in java the result of double types are always double two numbers are always without decimal
    }
}