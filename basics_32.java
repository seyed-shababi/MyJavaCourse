public class basics_32{
    public static void main(String[] args){
        int x = 10;
        int y = 4;
        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
        System.out.println("x + y = " + (x + y));
        System.out.println(("x - y = ") + (x - y));
        System.out.println(("x * y = ") + (x * y));
        System.out.println(("x / y = ") + (x / y));
        // in java the result of division of two numbers are always without decimal
        System.out.println(x / 3.0);
        System.out.println(1 + 0 + "0" + 5 * 10);
    }
}